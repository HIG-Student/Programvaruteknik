/**
 * How to group data
 */
public enum Resolution
{
    /**
     * Group into years
     */
    YEAR,

    /**
     * Group into quarters of a year
     */
    QUARTER,

    /**
     * Group into months
     */
    MONTH,

    /**
     * Group into weeks
     */
    WEEK,

    /**
     * Group into days
     */
    DAY
}
